	createMap() {
		for (let x = 0; x < 32; x++) {
			let x_array = [];
			for (let z = 0; z < 32; z++) {
				let y = this.heightMap[x][z];
				let y_array = [];

				for (let k = 0; k < 32; k++) {
					let ground = null;

					if (k < y) {
						// Terrain Block
						ground = new Cube();
						ground.matrix.translate(x, k, z);
						ground.textureNum = 0; // Using a single texture
					}

					y_array.push(ground);
				}

				x_array.push(y_array);
			}

			this.cubes.push(x_array);
		}
		this.placeTree(2, 23); // Place a tree at row 6, column 26
	}

	placeTree(x, z) {
		let baseY = this.heightMap[x][z]; // Get height from height map
		let trunkHeight = 3;
		let leafStart = baseY + trunkHeight;

		// --- 🌳 Create Trunk (3 wood blocks) ---
		for (let h = 0; h < trunkHeight; h++) {
			let trunk = new Cube();
			trunk.matrix.translate(x, baseY + h, z);
			trunk.textureNum = 0; // Same texture for trunk
			this.cubes[x][z][baseY + h] = trunk;
		}

		// --- 🍂 Create Leaves ---
		let radius = 3;
		for (let dx = -radius; dx <= radius; dx++) {
			for (let dz = -radius; dz <= radius; dz++) {
				for (let dy = 0; dy <= radius; dy++) {
					let leafX = x + dx;
					let leafY = leafStart + dy;
					let leafZ = z + dz;

					let distance = Math.sqrt(dx * dx + dy * dy + dz * dz);
					if (distance <= radius + Math.random() * 0.5) {
						if (
							leafX >= 0 &&
							leafX < 32 &&
							leafZ >= 0 &&
							leafZ < 32 &&
							leafY >= 0 &&
							leafY < 32
						) {
							let leaf = new Cube();
							leaf.matrix.translate(leafX, leafY, leafZ);
							leaf.textureNum = 0; // Same texture for leaves
							this.cubes[leafX][leafZ][leafY] = leaf;
						}
					}
				}
			}
		}
	}

	render() {
		for (let x = 0; x < 32; x++) {
			for (let z = 0; z < 32; z++) {
				for (let y = 0; y < 32; y++) {
					if (this.cubes[x][z][y] !== null) {
						this.cubes[x][z][y].render();
					}
				}
			}
		}
	}
